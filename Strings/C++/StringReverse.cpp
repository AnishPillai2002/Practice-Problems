#include <iostream>
#include <bits/stdc++.h>
using namespace std;
 
//Function to reverse a string
void reverse(string& str){
    int len=str.length();
    int n=len-1;
    int i=0;
    while(i<=n){
        swap(str[i],str[n]); // using swap()
        n = n-1;
        i = i+1;
  }
 
}
 
//Main function
int main(){

    string str;
    cout << "Enter the String: ";
    cin >> str;

    cout << "The Reverse of String : ";
    reverse(str);
    cout << str;
    return 0;
}
